m = int(input("Введите первое число (размер шоколадки по горизонтали): "))
n = int(input("Введите второе число (размер шоколадки по вертикали): "))
k = int(input("Введите третье число (число долек, которое необходимо отломить): "))

if k < m * n and ((k % m == 0) or (k % n == 0)):
    print("да, можно")
else:
    print("нет, нельзя")
